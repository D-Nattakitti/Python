print(f"คุณเห็นด้วยกลับนโยบายสำหรับ การให้ด่าคนในทีมได้หรือไม่?")

num = input("เลือก 0 ไม่เห็นด้วย หรือ 1 สำหรับเห็นด้วย : ")

int_num = int(num)

if int_num == 1:
    print(f"คุณเห็นด้วยกับนโยบายนี้")
elif int_num == 0:
    print(f"คุณไม่เห็นด้วยกับนโยบายนี้")
else :
    print(f"ค่าที่คุณใส่คือ {num} ซึ่งไม่ถูกต้อง!")



print(f"คุณเลือก: {num}")
